    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $id = $_GET['id'];
        return View :: make('TaggedLogEntries', [
            "tag_types" => TagType :: where("id", $id)->orderBy("created_at", "desc")->get()
        ]);

    }

